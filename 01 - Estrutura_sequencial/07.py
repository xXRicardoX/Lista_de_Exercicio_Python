# 7 - Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

quadrado = float(input('Digite o valor do quadrado: '))

area =  quadrado * quadrado
print(f'O valor da area do quadrado é  {area:.2f}\nSendo que o valor com o dobro da area seria {area*2:.2f}')