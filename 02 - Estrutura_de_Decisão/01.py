'''
    1 - Faça um Programa que peça dois números e imprima o maior deles.
'''

a1 = int(input("Digite um numero para o valor A: "))
a2 = int(input("Digite outro numero para o valor B: "))

if (a1 > a2):
    print(f"O valor de A {a1} é maior que o valor de B {a2}")
else:
    print(f"o valor de B {a2} é maior que o valor de A {a1}")